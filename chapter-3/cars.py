# sort a list alphabetically
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort()
print(cars)

# reverse a sort
cars.sort(reverse=True)
print(cars)

cars2 = ['bmw', 'audi', 'toyota', 'subaru']
print(cars2)
# use sorted() function to temporarily sort list
print(sorted(cars2))
print(cars2)

# use reverse() method to reverse order of list
cars2.reverse()
print(cars2)

# back to normal
cars2.reverse()
print(cars2)